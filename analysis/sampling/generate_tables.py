def generate_effn_requirements_table():
    """
    Generate effective sample size requirements table for discussion section.

    Shows eff_n required for different target rho values using the more
    conservative model for each distance type.
    """
    print("=" * 70)
    print("SECTION 1: EFFECTIVE SAMPLE SIZE REQUIREMENTS TABLE")
    print("=" * 70)

    timestamp = datetime.now().strftime("%Y-%m-%d")
    model_constants = load_model_constants()

    # Get model parameters for the more conservative model per distance type
    # Shortest path: use harmonic (requires more samples)
    shortest_a = model_constants["shortest"]["harmonic_model"]["A"]
    shortest_b = model_constants["shortest"]["harmonic_model"]["B"]

    # Angular: use betweenness (requires more samples)
    angular_a = model_constants["angular"]["betweenness_model"]["A"]
    angular_b = model_constants["angular"]["betweenness_model"]["B"]

    print("\nModel Parameters:")
    print(f"  Shortest (harmonic):    A = {shortest_a:.2f}, B = {shortest_b:.2f}")
    print(f"  Angular (betweenness):  A = {angular_a:.2f}, B = {angular_b:.2f}")

    # Target rho values
    target_rhos = [0.90, 0.95, 0.97, 0.99]

    # Compute eff_n requirements
    table_data = []
    for rho in target_rhos:
        shortest_eff_n = compute_required_eff_n(rho, shortest_a, shortest_b)
        angular_eff_n = compute_required_eff_n(rho, angular_a, angular_b)
        table_data.append(
            {
                "rho": rho,
                "shortest_eff_n": shortest_eff_n,
                "angular_eff_n": angular_eff_n,
            }
        )

    # Compute relative cost (ratio to 0.90 baseline)
    baseline_shortest = table_data[0]["shortest_eff_n"]
    for row in table_data:
        row["relative_cost"] = row["shortest_eff_n"] / baseline_shortest

    # Print computed values
    print("\nEffective Sample Size Requirements:")
    print(f"{'Target rho':<12} {'Shortest eff_n':<18} {'Angular eff_n':<18} {'Relative cost':<15}")
    print("-" * 70)
    for row in table_data:
        print(
            f"{row['rho']:<12.2f} {row['shortest_eff_n']:<18.0f} "
            f"{row['angular_eff_n']:<18.0f} {row['relative_cost']:.1f}x"
        )

    # Generate LaTeX table
    latex = f"""% Auto-generated table: Effective Sample Size Requirements
% Generated by generate_tables.py on {timestamp}
% DO NOT EDIT MANUALLY

\\begin{{tabular}}{{lrrr}}
\\toprule
Target $\\rhosp$ & Shortest $\\effn$ & Angular $\\effn$ & Relative cost \\\\
\\midrule
"""

    for row in table_data:
        rho_str = f"{row['rho']:.2f}"
        shortest_str = f"{row['shortest_eff_n']:.0f}"
        angular_str = f"{row['angular_eff_n']:.0f}"
        cost_str = f"{row['relative_cost']:.1f}$\\times$"
        latex += f"{rho_str} & {shortest_str} & {angular_str} & {cost_str} \\\\\n"

    latex += r"""\bottomrule
\end{tabular}
"""

    output_path = TABLES_DIR / "effn_requirements.tex"
    with open(output_path, "w") as f:
        f.write(latex)

    print(f"\nSaved: {output_path}")
    return table_data


