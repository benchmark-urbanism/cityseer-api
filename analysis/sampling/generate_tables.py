# Typical reachability values by distance (indicative, varies by network density)
TYPICAL_REACHABILITY = {
    200: 50,  # Short distance, limited connectivity
    500: 150,  # Local neighbourhood scale
    1000: 400,  # District scale
    2000: 1000,  # City-wide walking distance
    5000: 3000,  # Regional scale
}

# Reachability multipliers for range estimation
REACH_LOW_MULT = 0.5
REACH_HIGH_MULT = 2.0


def compute_required_p(target_rho: float, reachability: float, a: float, b: float) -> float:
    """Compute required sampling probability for target accuracy."""
    eff_n_required = compute_required_eff_n(target_rho, a, b)
    p = eff_n_required / reachability
    return min(1.0, max(0.0, p))


def compute_speedup(p: float) -> float:
    """Compute approximate speedup factor from sampling probability."""
    if p <= 0:
        return np.inf
    return 1.0 / p


def format_p_range(p_low: float, p_high: float) -> str:
    """Format probability range for table display."""
    if p_low >= 0.95:
        return "1.0 (full)"
    elif p_high >= 1.0:
        return f"{p_low:.2f}--1.0"
    else:
        return f"{p_low:.2f}--{p_high:.2f}"


def format_speedup_range(p_low: float, p_high: float) -> str:
    """Format speedup range for table display."""
    speedup_low = compute_speedup(p_high)  # Higher p = lower speedup
    speedup_high = compute_speedup(p_low)  # Lower p = higher speedup

    if p_low >= 0.95:
        return "1.0$\\times$"
    elif speedup_high >= 100:
        return f"{speedup_low:.0f}--{speedup_high:.0f}$\\times$"
    elif speedup_high >= 10:
        return f"{speedup_low:.0f}--{speedup_high:.0f}$\\times$"
    elif speedup_high >= 2:
        return f"{speedup_low:.1f}--{speedup_high:.1f}$\\times$"
    else:
        return f"{speedup_low:.1f}--{speedup_high:.1f}$\\times$"


def generate_practical_guidelines_table():
    """
    Generate practical guidelines table.

    Shows recommended sampling probabilities for common distance/reachability
    combinations to achieve target accuracy.
    """
    print("\n" + "=" * 70)
    print("SECTION 2: PRACTICAL GUIDELINES TABLE")
    print("=" * 70)

    timestamp = datetime.now().isoformat(timespec="seconds")
    model_constants = load_model_constants()

    # Get model parameters
    harmonic_a = model_constants["shortest"]["harmonic_model"]["A"]
    harmonic_b = model_constants["shortest"]["harmonic_model"]["B"]
    betweenness_a = model_constants["shortest"]["betweenness_model"]["A"]
    betweenness_b = model_constants["shortest"]["betweenness_model"]["B"]

    print("\nModel Parameters:")
    print(f"  Harmonic:    A = {harmonic_a:.2f}, B = {harmonic_b:.2f}")
    print(f"  Betweenness: A = {betweenness_a:.2f}, B = {betweenness_b:.2f}")

    # Determine which metric requires more samples (conservative)
    eff_n_h_95 = compute_required_eff_n(0.95, harmonic_a, harmonic_b)
    eff_n_b_95 = compute_required_eff_n(0.95, betweenness_a, betweenness_b)

    if eff_n_h_95 > eff_n_b_95:
        conservative_metric = "harmonic"
        conservative_a, conservative_b = harmonic_a, harmonic_b
        ratio = eff_n_h_95 / eff_n_b_95
        print(f"\nNote: Harmonic requires ~{ratio:.1f}x more samples than betweenness")
    else:
        conservative_metric = "betweenness"
        conservative_a, conservative_b = betweenness_a, betweenness_b
        ratio = eff_n_b_95 / eff_n_h_95
        print(f"\nNote: Betweenness requires ~{ratio:.1f}x more samples than harmonic")

    # Target accuracies
    targets = [0.95, 0.97]

    # Compute table data
    table_data = []
    for distance, typical_reach in TYPICAL_REACHABILITY.items():
        row = {
            "distance": distance,
            "typical_reach": typical_reach,
        }

        reach_low = int(typical_reach * REACH_LOW_MULT)
        reach_high = int(typical_reach * REACH_HIGH_MULT)

        for target in targets:
            p_at_high_reach = compute_required_p(target, reach_high, conservative_a, conservative_b)
            p_at_low_reach = compute_required_p(target, reach_low, conservative_a, conservative_b)
            row[f"p_{target}"] = (p_at_high_reach, p_at_low_reach)

        row["speedup"] = row["p_0.95"]
        table_data.append(row)

    # Print computed values
    print("\nComputed Guidelines Table Values:")
    print(f"{'Distance':<10} {'Reach':<15} {'p (rho=0.95)':<18} {'p (rho=0.97)':<18} {'Speedup':<15}")
    print("-" * 90)
    for row in table_data:
        p95_low, p95_high = row["p_0.95"]
        p97_low, p97_high = row["p_0.97"]
        speedup_low = compute_speedup(p95_high)
        speedup_high = compute_speedup(p95_low)
        print(
            f"{row['distance']:<10} ~{row['typical_reach']:<14} "
            f"{p95_low:.2f}--{min(1.0, p95_high):.2f}          "
            f"{p97_low:.2f}--{min(1.0, p97_high):.2f}          "
            f"{speedup_low:.1f}--{speedup_high:.1f}x"
        )

    # Generate LaTeX
    if conservative_metric == "harmonic":
        model_desc = f"$\\effn \\approx {harmonic_a:.0f}/\\epsilon - {harmonic_b:.0f}$"
        other_metric_note = f"Betweenness requires approximately {1/ratio:.1f}$\\times$ fewer samples."
    else:
        model_desc = f"$\\effn \\approx {betweenness_a:.0f}/\\epsilon - {betweenness_b:.0f}$"
        other_metric_note = f"Harmonic closeness requires approximately {1/ratio:.1f}$\\times$ fewer samples."

    latex = f"""% Auto-generated table: Practical Sampling Guidelines
% Generated by generate_tables.py on {timestamp}
% DO NOT EDIT MANUALLY - regenerate with: python generate_tables.py

\\begin{{table}}[htbp]
\\centering
\\caption{{Practical sampling guidelines for network centrality analysis. Shows recommended
sampling probabilities to achieve target rank correlation ($\\rho$) based on typical
reachability at each distance threshold. Values computed from {conservative_metric} model
({model_desc}, where $\\epsilon = 1 - \\rho$). {other_metric_note}
Actual reachability varies by network density; use probe sampling to estimate
local reachability before computation.}}
\\label{{tab:practical_guidelines}}
\\begin{{tabular}}{{rrllr}}
\\toprule
Distance (m) & Typical Reach & $p$ ($\\rho \\geq 0.95$) & $p$ ($\\rho \\geq 0.97$) & Speedup \\\\
\\midrule
"""

    for row in table_data:
        dist = row["distance"]
        reach = row["typical_reach"]
        p95_str = format_p_range(*row["p_0.95"])
        p97_str = format_p_range(*row["p_0.97"])
        speedup_str = format_speedup_range(*row["speedup"])

        latex += f"{dist} & $\\sim${reach} & {p95_str} & {p97_str} & {speedup_str} \\\\\n"

    latex += r"""\bottomrule
\end{tabular}

\vspace{0.5em}
\footnotesize
\textbf{Notes:}
\begin{itemize}[nosep,leftmargin=*]
\item Values use the more conservative metric (requiring more samples). The other metric achieves same accuracy with fewer samples.
\item Reachability varies significantly with network density. Dense urban cores may have 2$\times$ higher reachability; suburban areas may have 0.5$\times$.
\item Use \texttt{probe\_reachability()} to estimate actual reachability before computation.
\item At short distances ($\leq$500m), full computation may be faster than sampling overhead.
\end{itemize}
\end{table}
"""

    output_path = TABLES_DIR / "practical_guidelines.tex"
    with open(output_path, "w") as f:
        f.write(latex)

    print(f"\nSaved: {output_path}")
    return table_data


