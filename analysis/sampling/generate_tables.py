def load_model_constants():
    """Load official model constants from JSON file."""
    constants_path = OUTPUT_DIR / "sampling_model_constants.json"
    if constants_path.exists():
        with open(constants_path) as f:
            return json.load(f)
    else:
        raise FileNotFoundError(
            f"Model constants file not found: {constants_path}\n"
            "Run sampling_reach.py first to generate model constants."
        )


def compute_required_eff_n(target_rho: float, a: float, b: float) -> float:
    """
    Compute required effective sample size for target accuracy.

    From rho = 1 - A/(B + eff_n):
        eff_n = A/(1-rho) - B = A/epsilon - B

    Parameters
    ----------
    target_rho
        Target Spearman correlation (e.g., 0.95)
    a, b
        Model parameters

    Returns
    -------
    float
        Required effective sample size
    """
    epsilon = 1 - target_rho
    if epsilon <= 0:
        return float("inf")
    return a / epsilon - b


